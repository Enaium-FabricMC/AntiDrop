package cn.enaium.antidrop.mixin;

import cn.enaium.antidrop.AntiDrop;
import net.minecraft.client.Keyboard;
import org.lwjgl.glfw.GLFW;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(Keyboard.class)
public class KeyboardMixin {
    @Inject(at = @At("HEAD"), method = "onKey", cancellable = true)
    private void init(long window, int key, int scancode, int action, int modifiers, CallbackInfo ci) {
        if (action == GLFW.GLFW_PRESS) {
            var keyEvent = new AntiDrop.KeyEvent(key);
            AntiDrop.eventBus.post(keyEvent);
            if (keyEvent.isCancel()) {
                ci.cancel();
            }
        }
    }
}
